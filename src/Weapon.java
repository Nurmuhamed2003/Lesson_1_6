public class Weapon {
    private WeaponType laser;
    private WeaponType gun;
    private WeaponType knife;

    public WeaponType getLaser() {
        return laser;
    }

    public void setLaser(WeaponType laser) {
        this.laser = laser;
    }

    public WeaponType getGun() {
        return gun;
    }

    public void setGun(WeaponType gun) {
        this.gun = gun;
    }

    public WeaponType getKnife() {
        return knife;
    }

    public void setKnife(WeaponType knife) {
        this.knife = knife;
    }
}
